package it.unisalento.Model;

public class Autore {

	private String  nome;
	private String  cognome;
	
	
	public void setNomeAutore(String nome) {
		this.nome=nome;
	}
	public String getNomeAutore() {
		return nome;
	}
	
	public void setCognomeAutore(String cognome) {
		this.cognome=cognome;
	}
	public String getCognomeAutore() {
		return cognome;
	}
	
}
